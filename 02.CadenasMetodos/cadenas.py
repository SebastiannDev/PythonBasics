cadena1 = "Hola mundo en america"
cadena2 = "To python dev"

# lower
print(cadena1.lower())
print("---------")

# Upper
print(cadena1.upper())
print("---------")

# Capitalize
print(cadena1.capitalize())
print("---------")

# Split
print(cadena1.split())
print("---------")

# Replace
print(cadena1.replace("Hola", "Hello"))
print("---------")

# Find
print(cadena1.find("ndo"))
print("---------")

# Len
contar_caracteres = len(cadena1)
print(contar_caracteres)
print("---------")

# Count
print(cadena1.count('a'))
print("---------")
